// โจทย์ 2.2: พิมพ์เฉพาะสระ
// กำหนดข้อความต้นฉบับ
const message = "Hello World";

// เก็บสระในรูปตัวพิมพ์เล็กไว้ใน Set เพื่อให้การตรวจสอบเร็วและชัดเจน
const vowels = new Set(['a', 'e', 'i', 'o', 'u']);

// ใช้ for..of เพื่อวนตัวอักษรทีละตัวใน message
for (const ch of message) {
  // แปลงเป็นตัวพิมพ์เล็กก่อน แล้วเช็คว่าเป็นสระหรือไม่
  if (vowels.has(ch.toLowerCase())) {
    console.log(ch); // พิมพ์ตัวอักษรที่เป็นสระ
  }
}

// ตัวอย่างผลลัพธ์เมื่อรัน: e\n o
